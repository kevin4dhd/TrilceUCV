public class DatosNotificaciones {
    int Foto;
    String Nombre;
    String Carrera;
    String TokenUsuario;
    String Token_Device;
    String MiNombre;

    public DatosNotificaciones(int foto, String nombre, String carrera, String tokenUsuario, String token_Device, String miNombre) {
        Foto = foto;
        Nombre = nombre;
        Carrera = carrera;
        TokenUsuario = tokenUsuario;
        Token_Device = token_Device;
        MiNombre = miNombre;
    }
}
